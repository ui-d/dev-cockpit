// DevCockpit i18n — a tiny, dependency-free, DOM-free message catalogue.
//
// Chrome's native chrome.i18n resolves to the *browser* UI locale and can't be
// switched by a user setting, so we roll our own: a flat key→string table per
// language plus a t() lookup. This module touches no DOM and no chrome.* APIs,
// so both the page (app.js) and the service worker (background.js) can import it.
//
// Usage:
//   import { t, setLanguage, detectLang } from "./i18n.js";
//   setLanguage("pl");
//   t("view.boards");                 // "Tablice"
//   t("wx.setTo", { name: "Toruń" }); // "Ustawiono: Toruń."

export const LANGS = ["en", "pl"];

let current = "en";

// Guess the initial language from the browser. Polish if the UI is pl-*, else English.
export function detectLang() {
  try {
    return String(navigator?.language || "en").toLowerCase().startsWith("pl") ? "pl" : "en";
  } catch (e) {
    return "en";
  }
}

export function setLanguage(lang) {
  current = LANGS.includes(lang) ? lang : "en";
  return current;
}

export function getLanguage() {
  return current;
}

// Look up a key in the active language, falling back to English, then the key
// itself. `vars` values replace {placeholder} tokens in the string.
export function t(key, vars) {
  const table = MESSAGES[current] || MESSAGES.en;
  let s = table[key];
  if (s == null) s = MESSAGES.en[key];
  if (s == null) s = key;
  if (vars) for (const k in vars) s = s.split(`{${k}}`).join(String(vars[k]));
  return s;
}

